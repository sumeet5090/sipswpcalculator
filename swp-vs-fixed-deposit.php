                    Choose SWP Over FD?</h2>

                <h3 class="text-xl font-bold mt-6 mb-3 text-gray-800">Choose SWP If:</h3>
                <ul>
                    <li>Your retirement horizon is <strong>10+ years</strong></li>
                    <li>You're comfortable with moderate market volatility</li>
                    <li>You want higher post-tax income</li>
                    <li>You want your corpus to grow and beat inflation</li>
                    <li>You fall in the 20-30% tax bracket (SWP saves significantly more tax)</li>
                </ul>

                <h3 class="text-xl font-bold mt-6 mb-3 text-gray-800">Choose FD If:</h3>
                <ul>
                    <li>You need <strong>guaranteed, predictable income</strong></li>
                    <li>Your investment horizon is <strong>1-3 years</strong></li>
                    <li>You have very low risk tolerance</li>
                    <li>You're in the 5% tax bracket (tax impact is minimal)</li>
                    <li>You need emergency funds with partial withdrawal facility</li>
                </ul>


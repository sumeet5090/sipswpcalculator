                <!-- H2: What is SWP -->
                <h2 id="what-is-swp" class="text-3xl font-bold mb-4 text-gray-800">What Is a Systematic Withdrawal Plan
                    (SWP)?</h2>
                <p>
                    A <strong>Systematic Withdrawal Plan (SWP)</strong> allows you to withdraw a fixed or step-up amount
                    from your mutual fund investment
                    at regular intervals â€” monthly, quarterly, or annually. Unlike redeeming your entire investment, SWP
                    lets the remaining corpus
                    continue earning returns while providing <strong>regular income</strong>. This makes it the
                    preferred choice for retirees who want
                    their money to work even during withdrawals.
                </p>
                <p>
                    The key advantage: your corpus continues to generate returns (typically 10-12% in equity/hybrid
                    funds), so you're only withdrawing
                    a portion while the balance compounds. If your withdrawal rate stays below the return rate, your
                    corpus can actually <em>grow</em>
                    over time â€” something a Fixed Deposit can never do.
                </p>

                <!-- H2: What is FD -->
                <h2 id="what-is-fixed-deposit" class="text-3xl font-bold mt-10 mb-4 text-gray-800">What Is a Fixed
                    Deposit (FD)?</h2>
                <p>
                    A <strong>Fixed Deposit</strong> locks your money for a predetermined period (1-10 years) at a
                    guaranteed interest rate.
                    In 2026, major Indian banks offer FD rates of <strong>6.5-7.5%</strong> for general citizens and
                    <strong>7-8%</strong> for
                    senior citizens. FDs are insured up to $5,000 per depositor per bank by DICGC, making them one of
                    the safest investment options.
                </p>
                <p>
                    However, FD interest is <strong>fully taxable at your income tax slab rate</strong> (up to 30% +
                    surcharge), and returns
                    barely beat inflation. After adjusting for 6% inflation and 30% tax, your real return from an FD is
                    often
                    <strong>negative</strong>.
                </p>


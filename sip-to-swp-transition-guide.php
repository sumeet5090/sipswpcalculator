                    <h2 class="text-lg font-bold text-indigo-800 mb-3">📋 The Transition Strategy</h2>
                    <p class="text-gray-700 text-sm leading-relaxed">
                        Moving from accumulating wealth via <strong>SIPs</strong> to withdrawing it via
                        <strong>SWPs</strong> is not a switch you flip over night. The most dangerous time in an
                        investor's life is the "Retirement Red Zone" (3 years before to 3 years after retirement). To
                        bridge the gap safely, you must shift from a 100% aggressive growth mindset to a <strong>Bucket
                            Strategy</strong>: allocating 2-3 years of living expenses to ultra-safe liquid funds, while
                        keeping the rest in growth assets to combat inflation. You achieve this via an Equity Glide
                        Path, systematically rebalancing away from Small/Mid-cap funds into Balanced Advantage and Debt
                        funds without triggering massive LTCG/STCG taxes.
                    </p>
                </div>

                <p>
                    For 20 years, you did everything right. Every month, rain or shine, bull market or bear, your SIP
                    (Systematic Investment Plan) scooped up mutual fund units. The power of compounding worked its
                    magic. Now, you’ve hit your target corpus and you're ready to retire.
                </p>
                <p>
                    But the game is fundamentally about to change. <strong>Accumulation (saving) and Decumulation
                        (withdrawing) require entirely different skill sets.</strong> In accumulating, market crashes
                    are your friend—you buy units cheaper. In decumulating via an SWP (Systematic Withdrawal Plan),
                    market crashes are your enemy—you are forced to sell <em>more</em> units at rock-bottom prices just
                    to buy groceries.
                </p>

                <!-- 1. The Retirement Red Zone -->
                <h2 class="text-3xl font-bold mt-10 mb-4 text-gray-800">1. Surviving the "Retirement Red Zone"</h2>
                <p>
                    The "Red Zone" spans the 3 years leading up to your retirement and the first 3 years of it. If the
                    stock market drops 40% when you are 45, you shouldn't care; you just keep SIPing. If the stock
                    market drops 40% the year you retire, your entire plan is instantly derailed due to Sequence of
                    Returns Risk.
                </p>
                <p>
                    You cannot have 100% of your corpus in small-cap or pure mid-cap equity mutual funds on the day you
                    turn off your SIPs and turn on your SWPs.
                </p>

                <!-- 2. Implementing the Glide Path -->
                <h2 class="text-3xl font-bold mt-10 mb-4 text-gray-800">2. The Pre-Retirement Glide Path</h2>
                <p>
                    A "Glide Path" is how you smoothly land the plane. Starting 3 to 5 years before your retirement
                    date, you must begin systematically rebalancing your portfolio.
                </p>
                <ul>
                    <li><strong>Stop High-Risk SIPs:</strong> Redirect all new monthly SIP money into lower-volatility
                        assets: Debt Funds, Liquid Funds, or Arbitrage Funds.</li>
                    <li><strong>Tax-Harvesting Shift:</strong> Every year, redeem up to ₹1.25 Lakh of Long Term Capital
                        Gains (which is tax-free) from your equity funds and move that money into safer debt
                        instruments.</li>
                    <li><strong>Target Allocation:</strong> By the time you retire, your portfolio should roughly
                        resemble a 50/50 or 60/40 Equity-to-Debt split, rather than an 80/20 aggressive accumulation
                        split.</li>
                </ul>

                <!-- 3. The Bucket Strategy -->
                <h2 class="text-3xl font-bold mt-10 mb-4 text-gray-800">3. Setting up the SWP "Bucket Strategy"</h2>
                <p>
                    You should <em>never</em> run an SWP directly from a pure, volatile equity fund. Doing so means when
                    the market is down 20%, you are blindly selling units at a loss. Instead, use the <strong>Bucket
                        Strategy</strong>:
                </p>

                <h3 class="text-xl font-bold mt-6 mb-3 text-emerald-700">Bucket 1: Immediate Liquidity (Years 1 to 3)
                </h3>
                <p>
                    Hold roughly 2-3 years worth of your living expenses in this bucket.
                </p>
                <ul>
                    <li><strong>Instruments:</strong> Liquid Mutual Funds, Arbitrage Funds, or high-yield Bank FDs.</li>
                    <li><strong>Action:</strong> Your monthly SWP runs <strong>entirely from this bucket</strong>. It
                        has almost zero volatility. The market can crash 50% tomorrow, and your grocery money for the
                        next 3 years is completely safe.</li>
                </ul>

                <h3 class="text-xl font-bold mt-6 mb-3 text-blue-700">Bucket 2: Stability & Growth (Years 4 to 10)</h3>
                <p>
                    Hold about 30% to 40% of your portfolio here.
                </p>
                <ul>
                    <li><strong>Instruments:</strong> Balanced Advantage Funds (BAFs), Conservative Hybrid Funds, or
                        Corporate Bond Funds.</li>
                    <li><strong>Action:</strong> This bucket beats inflation by a small margin with moderate volatility.
                        Once a year, if Bucket 1 runs low, you systematically transfer money from Bucket 2 to refill it.
                    </li>
                </ul>

                <h3 class="text-xl font-bold mt-6 mb-3 text-purple-700">Bucket 3: Long-Term Legacy Growth (Years 11+)
                </h3>
                <p>
                    The remainder of your portfolio goes here.
                </p>
                <ul>
                    <li><strong>Instruments:</strong> Flexi-Cap, Large-Cap, and Mid-Cap pure equity funds.</li>
                    <li><strong>Action:</strong> Do not touch this money for a decade. Let it compound massively. When
                        the market makes new all-time highs, you can scoop "profit" off the top of Bucket 3 and use it
                        to refill Bucket 2.</li>
                </ul>


                <h2>LTCG vs STCG: How Holding Period Affects Tax</h2>
                <p><strong>Long-Term Capital Gains (LTCG)</strong> on equity mutual funds (held >1 year) are taxed at a
                    flat rate of <strong>12.5%</strong> on gains exceeding $1,500 in a financial year. This is one
                    of the most favorable tax treatments available for investments worldwide.</p>
                <p><strong>Short-Term Capital Gains (STCG)</strong> on equity funds (held ≤1 year) are taxed at
                    <strong>20%</strong>. For debt funds purchased after April 1, 2023, all gains are treated as
                    short-term regardless of holding period and taxed at your income slab rate.
                </p>

                <h2>Tax Treatment of SIP Investments (FIFO Method)</h2>
                <p>Each SIP installment is treated as a <strong>separate purchase</strong> by the Income Tax Department.
                    When you redeem or set up an SWP, units are sold on a First-In-First-Out (FIFO) basis:</p>
                <ul>
                    <li>Units from your earliest SIP installments are sold first</li>
                    <li>If those units were purchased >12 months ago, the gain qualifies as LTCG</li>
                    <li>If <12 months old, it's STCG</li>
                </ul>
                <p><strong>Planning tip:</strong> Always ensure your earliest SIP installments have crossed the 1-year
                    mark before starting an SWP to maximize LTCG treatment.</p>


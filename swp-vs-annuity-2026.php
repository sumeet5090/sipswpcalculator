                <!-- H2: Best Strategy -->
                <h2 id="optimal-strategy" class="text-3xl font-bold mt-10 mb-4 text-gray-800">5. The Verdict: The "Floor
                    & Upside" Strategy</h2>
                <p>
                    You do not have to pick an extreme path. In fact, most financial planners recommend a hybrid
                    approach to combine the psychological safety of an annuity with the wealth-building power of an SWP.
                </p>
                <p>
                    This is known as the <strong>Floor and Upside Strategy</strong>:
                </p>
                <ul>
                    <li><strong>The Floor (Annuity):</strong> Calculate your <em>bare-minimum survival expenses</em>
                        (groceries, utilities, insurance premiums). Buy an annuity just large enough that its guaranteed
                        payout covers these absolute necessities. This guarantees you will never be entirely broke.</li>
                    <li><strong>The Upside (SWP):</strong> Take the remaining 60-70% of your corpus and invest it in a
                        diversified Mutual Fund portfolio. Set up an SWP to fund your <em>lifestyle expenses</em>
                        (travel, dining, gifts). Over the years, increase this SWP to battle inflation, while letting
                        the underlying corpus grow and build a legacy.</li>
                </ul>


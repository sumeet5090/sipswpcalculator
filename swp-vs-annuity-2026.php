                    <h2 class="text-lg font-bold text-indigo-800 mb-3">📋 Quick Summary: SWP vs Annuity</h2>
                    <p class="text-gray-700 text-sm leading-relaxed">
                        For Indian retirees in 2026, a <strong>Systematic Withdrawal Plan (SWP)</strong> from mutual
                        funds generally offers superior wealth generation, tax efficiency, and liquidity compared to an
                        <strong>Immediate Annuity</strong>.
                        While Annuities provide an absolute risk-free, guaranteed lifelong payout (typically yielding
                        5.5% to 6.5%), their payouts are fully taxable and lose value against inflation over decades. In
                        contrast, an SWP from a hybrid or equity portfolio typically targets 10-12% returns, allowing
                        the <em>underlying capital to grow</em> even as you withdraw. Plus, SWP withdrawals are highly
                        tax-efficient: only the capital gains portion is taxed at 12.5% (LTCG).
                        <strong>Best strategy:</strong> Cover basic, non-discretionary living expenses with a guaranteed
                        Annuity (or fixed deposit equivalent), and use an SWP to fund lifestyle expenses, combat
                        inflation, and build legacy wealth.
                    </p>

                <h2>The Contender Breakdown: Deep Dives</h2>

                <h3
                    class="text-2xl font-bold mt-10 mb-4 text-indigo-900 border-b border-indigo-100 pb-2 flex items-center gap-3">
                    <span class="bg-indigo-100 text-indigo-800 px-3 py-1 rounded-lg text-sm">Asset 1</span> Equity SIPs
                    (Systematic Investment Plans)
                </h3>
                <p>An equity SIP is not an asset class itself; it is a <em>method</em> of buying into equity mutual
                    funds systematically. It represents ownership in real, producing businesses globally or
                    domestically.</p>
                <div class="grid md:grid-cols-2 gap-6 my-6 not-prose">
                    <div class="bg-emerald-50 border border-emerald-100 p-5 rounded-xl">
                        <h4 class="text-emerald-800 font-bold mb-3 flex items-center gap-2"><svg class="w-5 h-5"
                                fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2"
                                    d="M5 13l4 4L19 7"></path>
                            </svg> The Bull Case</h4>
                        <ul class="space-y-2 text-sm text-emerald-900">
                            <li><strong>The Inflation Killer:</strong> Historically, broad-market equities are the only
                                asset class known to persistently outpace inflation by 5-7%.</li>
                            <li><strong>Rupee/Dollar Cost Averaging:</strong> SIPs automate the psychological hurdle of
                                market timing. You buy more units when markets crash, lowering your average cost.</li>
                            <li><strong>Ultimate Liquidity:</strong> Outside of ELSS funds, you can hit "Sell" on your
                                brokerage app and have cash in your bank in T+2 days.</li>
                        </ul>
                    </div>
                    <div class="bg-rose-50 border border-rose-100 p-5 rounded-xl">
                        <h4 class="text-rose-800 font-bold mb-3 flex items-center gap-2"><svg class="w-5 h-5"
                                fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2"
                                    d="M6 18L18 6M6 6l12 12"></path>
                            </svg> The Bear Case</h4>
                        <ul class="space-y-2 text-sm text-rose-900">
                            <li><strong>Short-Term Volatility:</strong> The market can and will drop 20-30% during
                                global shocks. If your time horizon is under 3 years, you might withdraw less than you
                                invested.</li>
                            <li><strong>Psychological Tax:</strong> Requires extreme emotional discipline to not pause
                                or cancel the SIP during heavy bear markets.</li>
                        </ul>
                    </div>
                </div>

                <h3
                    class="text-2xl font-bold mt-10 mb-4 text-gray-800 border-b border-gray-200 pb-2 flex items-center gap-3">
                    <span class="bg-gray-200 text-gray-800 px-3 py-1 rounded-lg text-sm border border-gray-300">Asset
                        2</span> Public Provident Fund (PPF)
                </h3>
                <p>The PPF is a government-backed, fixed-income scheme in India. It is considered the gold standard of
                    "safe money" for one primary reason: <strong>EEE Status</strong>.</p>
                <ul class="space-y-2 mb-6">
                    <li><strong>E (Exempt):</strong> The money you invest gives you a tax deduction up to $1.5 Lakhs.
                    </li>
                    <li><strong>E (Exempt):</strong> The 7.1% interest you earn every year is completely tax-free.</li>
                    <li><strong>E (Exempt):</strong> After 15 years, the entire accumulated maturity amount is withdrawn
                        100% tax-free.</li>
                </ul>
                <p><strong>The Strategy:</strong> Do not treat PPF as a wealth creator. Treat it as the fixed-income
                    backbone of your portfolio. If the stock market crashes 40% the year you retire, your PPF acts as
                    the bedrock that prevents a total portfolio collapse.</p>

                <h3
                    class="text-2xl font-bold mt-10 mb-4 text-amber-800 border-b border-amber-100 pb-2 flex items-center gap-3">
                    <span class="bg-amber-100 text-amber-800 px-3 py-1 rounded-lg text-sm border border-amber-200">Asset
                        3</span> Bank Fixed Deposits (FDs)
                </h3>
                <p>Fixed deposits are exactly what they sound like: you lock your capital with a bank for a fixed tenure
                    at a fixed rate. While beloved by previous generations, modern tax laws and inflation have broken
                    the math for long-term FD wealth creation.</p>
                <div class="bg-white p-6 rounded-xl border-l-4 border-amber-500 shadow-sm my-6 text-gray-700 italic">
                    "If you are in the 30% tax bracket, a 7% FD yields a 4.9% post-tax return. If inflation is 6%, your
                    real return is -1.1%. You are mathematically guaranteeing that you will become poorer over time."
                </div>
                <p><strong>The Strategy:</strong> FDs should strictly be used for two things: <strong>1) Emergency
                        Funds</strong> (3-6 months of expenses requiring instant liquidity), and <strong>2) Ultra
                        Short-Term Goals</strong> (down payments required within 1-2 years where capital preservation is
                    paramount).</p>

                <h3
                    class="text-2xl font-bold mt-10 mb-4 text-emerald-800 border-b border-emerald-100 pb-2 flex items-center gap-3">
                    <span
                        class="bg-emerald-100 text-emerald-800 px-3 py-1 rounded-lg text-sm border border-emerald-200">Asset
                        4</span> National Pension System (NPS)
                </h3>
                <p>NPS is a hybrid vehicle bridging the gap between PPF safety and SIP growth. It invests your money
                    across Equity (up to 75%), Corporate Bonds, and Government Bonds.</p>
                <p><strong>The Catch:</strong> It is highly illiquid. Money is locked until age 60. Then, you can only
                    withdraw 60% as a tax-free lump sum. The remaining 40% <em>must</em> be used to purchase a highly
                    inefficient annuity product from a life insurance company.</p>



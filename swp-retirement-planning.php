                <h2 class="text-3xl font-bold mt-12 mb-4 text-rose-800 border-b border-rose-200 pb-2">The Ultimate
                    Danger: Sequence of Return Risk (SORR)</h2>
                <p>The single greatest threat to a retiree using an SWP is not low average returns, but <em>when</em>
                    those low returns happen. This is mathematically defined as <strong>Sequence of Return Risk
                        (SORR)</strong>.</p>
                <p>Imagine you retire with $1 Million and execute a $40,000/year SWP. If the stock market crashes 30% in
                    your <em>first</em> year of retirement (Corpus drops to $700K, then you withdraw $40K leaves $660K
                    remaining), your portfolio sustains permanent, unrecoverable damage. It no longer has enough
                    critical mass to bounce back when the market eventually recovers. Compare this to someone who gets
                    the same 30% crash in Year 15 of retirement — by then their corpus has already doubled through
                    compounding and can easily absorb the hit.</p>
                <div class="bg-rose-50 border-l-4 border-rose-500 p-5 rounded-r-xl my-6 not-prose">
                    <p class="text-rose-900 font-bold text-sm">The Uncomfortable Truth About SORR</p>
                    <p class="text-rose-800 text-sm mt-2">Two retirees can have the <em>exact same</em> average return
                        over 25 years, but the one who experienced poor returns early can run out of money while the
                        other still has millions left. The order matters more than the average.</p>
                </div>

                <h3 class="text-2xl font-bold mt-8 mb-3 text-indigo-900">The Institutional Solution: The 3-Bucket
                    Strategy</h3>
                <p>To completely neutralize SORR, institutional wealth managers never execute SWPs directly from a 100%
                    volatile equity portfolio. They design a "Bucket Architecture" to ensure you never need to sell
                    equity during a crash.</p>

